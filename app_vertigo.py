def guardar_memoria():
    """Envía el estado actual del chat a Supabase."""
    url = f"{SUPABASE_URL.rstrip('/')}/rest/v1/chats"
    
    # Preparamos el paquete de datos
    datos = {
        "id": st.session_state.chat_actual,
        "usuario_id": st.session_state.usuario_id,
        "mensajes": st.session_state.messages,
    }
    
    # El header 'Prefer': 'resolution=merge-duplicates' es clave para que 
    # Supabase sepa que si el ID ya existe, debe SOBRESCRIBIR los mensajes.
    try:
        res = requests.post(url, headers=headers, json=datos)
        if res.status_code not in [200, 201, 204]:
            st.error(f"Error al sincronizar: {res.text}")
    except Exception as e:
        st.error(f"Fallo de conexión con la nube: {e}")

